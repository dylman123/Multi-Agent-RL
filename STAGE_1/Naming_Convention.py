# Inputs an integer from 1 to 26 and outputs the corresponding alphabetic letter
def integer_to_letter(integer):

    if integer == 1:
        return 'A'
    elif integer == 2:
        return 'B'
    elif integer == 3:
        return 'C'
    elif integer == 4:
        return 'D'
    elif integer == 5:
        return 'E'
    elif integer == 6:
        return 'F'
    elif integer == 7:
        return 'G'
    elif integer == 8:
        return 'H'
    elif integer == 9:
        return 'I'
    elif integer == 10:
        return 'J'
    elif integer == 11:
        return 'K'
    elif integer == 12:
        return 'L'
    elif integer == 13:
        return 'M'
    elif integer == 14:
        return 'N'
    elif integer == 15:
        return 'O'
    elif integer == 16:
        return 'P'
    elif integer == 17:
        return 'Q'
    elif integer == 18:
        return 'R'
    elif integer == 19:
        return 'S'
    elif integer == 20:
        return 'T'
    elif integer == 21:
        return 'U'
    elif integer == 22:
        return 'V'
    elif integer == 23:
        return 'W'
    elif integer == 24:
        return 'X'
    elif integer == 25:
        return 'Y'
    elif integer == 26:
        return 'Z'
    else:
        return 'Number is out of range'
